package Java_Basic_Concepts;

public class JAVA_Variables {
    public static void main(String[] args) {
        //A variable is a location in memory (storage area) to hold data.
        //To indicate the storage area, each variable should be given a unique name (identifier).

        int marksScored = 86; //One Way Of Assigning the value to the variable

        int expectedScore;
        expectedScore=94; //Another Way Of Assigning Value to the variable
    }
}
//Java is a statically-typed language. It means that all variables must be declared before they can be used.
//The value of a variable can be changed in the program, hence the name variable.
//However, we cannot change the datatype of a variable in Java within the same scope.
//int x=10; --This works because we haven't used x previously.
//float x=10; --Does not work

//Java is case-sensitive.
//Variables must start with either a letter or an underscore, _ or a dollar, $ sign.
//Variable names cannot start with numbers.
//Variable names can't use whitespace.

